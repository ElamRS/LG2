package Complex_Diagram;

public class AlignedSegment {
	// Atributos
	private int refStart;
	private int refEnd;
	private int memberStart;
	private int memberEnd;
	
	// Métodos de Acesso
	public int getRefStart() {
		return refStart;
	}
	public void setRefStart(int refStart) {
		this.refStart = refStart;
	}
	public int getRefEnd() {
		return refEnd;
	}
	public void setRefEnd(int refEnd) {
		this.refEnd = refEnd;
	}
	public int getMemberStart() {
		return memberStart;
	}
	public void setMemberStart(int memberStart) {
		this.memberStart = memberStart;
	}
	public int getMemberEnd() {
		return memberEnd;
	}
	public void setMemberEnd(int memberEnd) {
		this.memberEnd = memberEnd;
	}
}
