package Complex_Diagram;

public class PatternLocation {
	// Atributos
	private short refStart;
	private short refEnd;
	private String pattern;
	
	// Métodos de Acesso
	public short getRefStart() {
		return refStart;
	}
	public void setRefStart(short refStart) {
		this.refStart = refStart;
	}
	public short getRefEnd() {
		return refEnd;
	}
	public void setRefEnd(short refEnd) {
		this.refEnd = refEnd;
	}
	public String getPattern() {
		return pattern;
	}
	public void setPattern(String pattern) {
		this.pattern = pattern;
	}
}
